package com.change.hero;

public class Text {
    int textPositionX;
    int textPositionY;
    int textWidth;
    int textHeight;

    public Text(int textPositionX, int textPositionY, int textWidth, int textHeight) {
        this.textPositionX = textPositionX;
        this.textPositionY = textPositionY;
        this.textWidth = textWidth;
        this.textHeight = textHeight;
    }
}
